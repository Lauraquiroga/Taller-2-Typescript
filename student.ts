export class Student {
    name: string;
    code: string;
    id: string;
    age: string;
    address: string;
    telephone: string;
  
    constructor(name: string, code: string, id: string, age: string, address: string, telephone: string) {
      this.name = name;
      this.code = code;
      this.address=address;
      this.age=age;
      this.id=id;
      this.telephone=telephone;
      
    }
  }
  